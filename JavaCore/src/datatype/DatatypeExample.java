package datatype;

public class DatatypeExample {
	public static void main(String[] args) {
		
	// all data types
	// declaration  data type name variable name = value
	String name = "anne";
	byte a=2;
	short b=20;
	int c=23456;
	long d=345678L;
	
	float x=12.55f;
	double y=645.66;
	
	char ans='Y';
	
	System.out.println("String type: "+name);
	System.out.println("Byte type: "+a);
	System.out.println("Short type: "+b);
	System.out.println("Int type: "+c);
	System.out.println("Long type: "+d);
	System.out.println("Float type: "+x);
	System.out.println("Double type: "+y);
	System.out.println("Char type: "+ans);
	}

}
